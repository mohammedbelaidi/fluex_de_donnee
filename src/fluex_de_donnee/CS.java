         public class MCare{
        
        int nbrIndividu = Integer.parseInt(ni.getText());
        int nbrVariable = Integer.parseInt(nv.getText());
        double matrix[][] = new double[nbrIndividu][nbrVariable];
        
        public void sais(){
            for(int i=0;i<nbrIndividu;i++){
                for(int j=0; j<nbrVariable; j++){
                    matrix[i][j] = Double.parseDouble(String.valueOf(tab.getValueAt(i, j)));
                }
            }
        }
        
        public double moyenX(){
            double som = 0;
            for(int i=0; i<nbrIndividu; i++){
                som += matrix[i][0];
            }
            return som/nbrIndividu;
        }
        
        public double moyenY(){
            double som = 0;
            for(int i=0; i<nbrIndividu; i++){
                som += matrix[i][1];
            }
            return som/nbrIndividu;
        }
        
        public double varianceX(){
            double mx = moyenX();
            double som = 0;
            for(int i=0; i<nbrIndividu; i++){
                som += Math.pow(matrix[i][0]-mx,2);
            }
            return som/nbrIndividu;
        }
        
        public double varianceY(){
            double my = moyenY();
            double som = 0;
            for(int i=0; i<nbrIndividu; i++){
                som += Math.pow(matrix[i][1]-my,2);
            }
            return som/nbrIndividu;
        }
        
        public double couvarianceXY(){
            double som = 0;
            double mx = moyenX();
            double my = moyenY();
            for(int i=0; i<nbrIndividu; i++){
                som += (matrix[i][0]-mx)*(matrix[i][1]-my);
            }
            return som/nbrIndividu;
        }
        
        public double cofficientC(){
            double cv = couvarianceXY();
            double ex = Math.sqrt(varianceX());
            double ey = Math.sqrt(varianceY());
            return cv/(ex*ey);
        }
        
        public String droitYX(){
            double a = couvarianceXY() / varianceX();
            double b = moyenY() - (a * moyenX());
            return "(" + getFormat(a) + ")x + (" + getFormat(b) + ")";
        }
        
        public String droitXY(){
            double a = couvarianceXY() / varianceY();
            double b = moyenX() - (a * moyenY());
            return "(" + getFormat(1/a) + ")x + (" + getFormat(-b/a) + ")";
        }
        
    }

    
