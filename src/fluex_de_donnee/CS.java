    class Acp extends MCare{
        double sx,sy;
        public Acp(double sx,double sy){
            super();
            this.sx = sx;
            this.sy = sy;
        }
      
        double [][]x(){
              mCare.sais();
                    
                    double[][] r = mCare.matrix;
                    double[][] x = new double[nbrIndividu][nbrVariable];
                    double mx = mCare.moyenX();
                    double my = mCare.moyenY();
                    for(int i=0; i<nbrIndividu; i++){
                        for(int j=0; j<nbrVariable; j++){
                            if(j == 0)
                                x[i][j] = (r[i][j] - mx)/(Math.sqrt(nbrIndividu) * sx);
                            else
                                x[i][j] = (r[i][j] - my)/(Math.sqrt(nbrIndividu) * sy);
                        }
                    }
                
            return x;    
        }
        double [][]xReverse(){
            double [][]xReverse = new double[nbrVariable][nbrIndividu];
            for(int i = 0;i<nbrVariable ; i++){
                for(int j = 0; j < nbrIndividu ; j++){
                    xReverse[i][j] = mCare.matrix[j][i];
                }
            }
            return xReverse;
        }
        
       double[][] matrixXx(){
            double[][] xx = new double[nbrVariable][nbrVariable];
            double[][] x = x();
            double[][] xReverse = xReverse();
            for(int i=0; i<nbrVariable; i++){
                for(int j=0; j<nbrVariable; j++){
                    xx[i][j] = 0;
                    for(int k=0; k<nbrIndividu; k++){
                        xx[i][j] = xx[i][j] + (xReverse[i][k] * x[k][j]);
                    }
                }
            }
            return xx;
        }
         double h1,h2;
        double [][]vp(){
            MRectongle mr = new MRectongle(matrixXx()[0][1],matrixXx()[0][0],matrixXx()[1][1]
                    ,mCare.moyenX(),mCare.moyenY());
            double h1,h2;
            h1 = Math.max(mr.landa1(), mr.landa2());
            h2 = Math.min(mr.landa1(), mr.landa2());
            double u[][] = new double[nbrVariable][nbrVariable];
            u[0][0] = mr.alph1(h1);
            u[1][0] = mr.alph2(h1);
            u[0][1] = mr.alph2(h2);
            u[1][1] = mr.alph2(h2);
            return u;
           
        }
         double [][]xu(){
            
             double prod[][] = new double[nbrIndividu][nbrVariable];
             for(int i = 0; i<nbrIndividu; i++){
                 for(int j = 0;j < nbrVariable;j++){
                     prod[i][j] = 0;
                     for(int n = 0;n <nbrVariable; n++){
                         prod[i][j] = prod[i][j] + x()[i][n] * vp()[n][j]; 
                     }
                 }
             }
             return prod;
         }
         
         double [][]xU(){
             double mxu[][] = new double[nbrVariable][nbrVariable];
             for(int i = 0; i<vp().length;i++){
                 for(int j = 0;j<vp().length;j++){
                     if(j == 0){
                         mxu[i][j] = vp()[i][j] * Math.sqrt(h1);
                     }else{
                         mxu[i][j] = vp()[i][j] * Math.sqrt(h2);
                     }
                 }
             }
             
            
             
        return mxu;
    }
         
        }
    
        
    
