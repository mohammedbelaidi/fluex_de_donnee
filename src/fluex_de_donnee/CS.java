        int nbrVariable = Integer.parseInt(nv.getText());
        double matrix[][] = new double[nbrIndividu][nbrVariable];
        public MRectongle(double cov, double vX, double vY, double mX, double mY){
            
            this.mX = mX;
            this.mY = mY;
            
            matrix[0][0] = vX;
            matrix[0][1] = cov;
            matrix[1][0] = cov;
            matrix[1][1] = vY;
        }
       
        double b ,land1,land2;
        double c;
       
      
        
       public double a(){
           
            return 1;
        }
        
        public double b(){
            return -(matrix[0][0] + matrix[1][1]);
        }
        
        public double c(){
            return (matrix[0][0] * matrix[1][1]) - (matrix[0][1] * matrix[1][0]);
        }
        
        public double delta(){
            return Math.pow(b(),2) - 4 * (a() * c());
        }
        
        public double landa1(){
            return (-b()-Math.sqrt(delta()))/(2*a());
        }
        
        public double landa2(){
            return (-b()+Math.sqrt(delta()))/(2*a());
        }
        
        double alph1(double x){
            return (alph2(x) * matrix[0][1]) / (matrix[0][0] - x);
        }
        double alph2(double x){
            return 1 / Math.sqrt(((Math.pow(matrix[0][1] / (matrix[0][0] - x), 2))+1));
        }
